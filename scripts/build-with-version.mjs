import fs from "fs";
import path from "path";
import { spawnSync } from "child_process";

const root = process.cwd();

const versionFile = path.join(root, "version.json");
const appVersionFile = path.join(root, "src", "lib", "appVersion.ts");

function readVersion() {
  if (!fs.existsSync(versionFile)) {
    return { major: 1, minor: 0 };
  }

  const raw = fs.readFileSync(versionFile, "utf8");
  return JSON.parse(raw);
}

function writeAppVersion(version) {
  const displayVersion = `V${version.major}.${version.minor}`;

  const content = `// Auto-generated by scripts/build-with-version.mjs
export const APP_VERSION = "${displayVersion}";
`;

  fs.writeFileSync(appVersionFile, content, "utf8");
}

function writeNextVersion(version) {
  const nextVersion = {
    major: version.major,
    minor: version.minor + 1,
  };

  fs.writeFileSync(
    versionFile,
    JSON.stringify(nextVersion, null, 2) + "\n",
    "utf8",
  );
}

const version = readVersion();

writeAppVersion(version);

const result = spawnSync("npx", ["vite", "build"], {
  stdio: "inherit",
  shell: true,
});

if (result.status === 0) {
  writeNextVersion(version);
  console.log(`\nBuild successful. Current public version: V${version.major}.${version.minor}`);
  console.log(`Next build version prepared: V${version.major}.${version.minor + 1}\n`);
  process.exit(0);
}

console.error("\nBuild failed. Version was not incremented.\n");
process.exit(result.status || 1);